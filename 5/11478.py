def cc(x, y):
    s = ''
    while x > 0:
        s = str(x % y) + s
        x = x // y
    return s
a = []
g = []
for i in range(1, 100):
    b = cc(i, 3)
    if int(b) % 2 == 0:
        b = '1' + b + '00'
    else:
        b = b + cc(sum(map(int, b)), 3)
    r = int(b)
    a.append(r)
    g.append(i)
for f in sorted(a):
    if f > 168:
        print(g[a.index(f)]) # 10
        break
print(sorted(a))
